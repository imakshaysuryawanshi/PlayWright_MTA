// For Loop Help you to repeat a block of code.
for (let i = 0; i < 10; i++) {
    console.log(i);
}
console.log("--------------");
for (let somya = 0; somya < 3; somya++) { // 0 to 9, Times -> 10
    console.log(somya);
}
console.log("--------------");
for (let _1 = 5; _1 >= 0; _1--) { // 0 to 10, Times -> 11
    console.log(_1);
}
console.log("--------------");